export type Language = keyof typeof translations

export function getTranslation(language: Language, key: keyof typeof translations.en): string {
  return translations[language][key] || translations.en[key]
}

// Get all translations for a language
export function getAllTranslations(language: Language | 'auto') {
  const selectedLanguage: Language = language === 'auto' ? detectLanguage() : language
  return translations[selectedLanguage]
}

// Auto-detect browser language
export function detectLanguage(): Language {
  if (typeof window === 'undefined') return 'en'

  const browserLang = navigator.language.toLowerCase()

  // Check for French variants
  if (browserLang.startsWith('fr')) {
    return 'fr'
  }

  // Check for Spanish variants
  if (browserLang.startsWith('es')) {
    return 'es'
  }

  // Check for German variants
  if (browserLang.startsWith('de')) {
    return 'de'
  }

  // Check for Portuguese variants (pt-BR, pt-PT, etc.)
  if (browserLang.startsWith('pt')) {
    return 'pt'
  }

  // Check for Japanese
  if (browserLang.startsWith('ja')) {
    return 'ja'
  }

  // Check for Chinese Simplified (zh-CN, zh-Hans, zh)
  if (browserLang.startsWith('zh')) {
    return 'zh'
  }

  // Check for Korean
  if (browserLang.startsWith('ko')) {
    return 'ko'
  }

  // Check for Arabic variants
  if (browserLang.startsWith('ar')) {
    return 'ar'
  }

  // Check for Hindi
  if (browserLang.startsWith('hi')) {
    return 'hi'
  }

  // Check for Dutch
  if (browserLang.startsWith('nl')) {
    return 'nl'
  }

  // Check for Swedish
  if (browserLang.startsWith('sv')) {
    return 'sv'
  }

  // Check for Norwegian (Bokmål) — browsers may report 'no' or 'nb'
  if (browserLang.startsWith('nb') || browserLang.startsWith('no')) {
    return 'nb'
  }

  // Check for Danish
  if (browserLang.startsWith('da')) {
    return 'da'
  }

  // Check for Italian
  if (browserLang.startsWith('it')) {
    return 'it'
  }

  // Check for Finnish
  if (browserLang.startsWith('fi')) {
    return 'fi'
  }

  // Default to English
  return 'en'
}

// Apply translations to banner config text
export function applyTranslations(language: Language | 'auto') {
  let selectedLanguage: Language = language === 'auto' ? detectLanguage() : language

  return {
    title: translations[selectedLanguage].title,
    message: translations[selectedLanguage].message,
    acceptButton: translations[selectedLanguage].acceptButton,
    rejectButton: translations[selectedLanguage].rejectButton,
    preferencesButton: translations[selectedLanguage].preferencesButton,
  }
}
